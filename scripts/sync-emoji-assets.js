/**
 * 将 emoji 转换为 Unicode codepoint
 */
function getEmojiCodepoint(emoji) {
  return [...emoji]
    .map(c => c.codePointAt(0).toString(16).toLowerCase())
    .join('-');
}

/**
 * 下载单个文件
 */
function downloadFile(url, outputPath) {
  return new Promise((resolve, reject) => {
    const protocol = url.startsWith('https:') ? https : http;

    protocol.get(url, (response) => {
      if (response.statusCode !== 200) {
        reject(new Error(`HTTP ${response.statusCode}: ${url}`));
        return;
      }

      const fileStream = fs.createWriteStream(outputPath);
      response.pipe(fileStream);

      fileStream.on('finish', () => {
        fileStream.close();
        resolve(outputPath);
      });

      fileStream.on('error', (err) => {
        fs.unlink(outputPath, () => {}); // 删除部分下载的文件
        reject(err);
      });
    }).on('error', (err) => {
      reject(err);
    });
  });
}

/**
 * 同步 Twemoji 资源
 */
async function syncTwemoji(emojiList = CONFIG.GENERATOR_EMOJIS) {
  console.log('🚀 开始同步 Twemoji 资源...');
  console.log(`📋 需要同步 ${emojiList.length} 个 emoji`);

  // 确保目录存在
  if (!fs.existsSync(CONFIG.LOCAL_DIR)) {
    fs.mkdirSync(CONFIG.LOCAL_DIR, { recursive: true });
    console.log(`📁 创建目录: ${CONFIG.LOCAL_DIR}`);
  }

  let success = 0;
  let failed = 0;
  const failedEmojis = [];

  for (let i = 0; i < emojiList.length; i++) {
    const emoji = emojiList[i];
    const codepoint = getEmojiCodepoint(emoji);
    const url = `${CONFIG.TWEMOJI_CDN}${codepoint}.png`;
    const outputPath = path.join(CONFIG.LOCAL_DIR, `${codepoint}.png`);

    process.stdout.write(`\r⏳ 进度: ${i + 1}/${emojiList.length} (${Math.round((i + 1) / emojiList.length * 100)}%) - ${emoji}`);

    try {
      // 检查文件是否已存在
      if (fs.existsSync(outputPath)) {
        success++;
        continue;
      }

      await downloadFile(url, outputPath);
      success++;
    } catch (error) {
      failed++;
      failedEmojis.push({ emoji, codepoint, error: error.message });
    }
  }

  console.log('\n');
  console.log('📊 同步完成统计:');
  console.log(`✅ 成功: ${success}/${emojiList.length}`);
  console.log(`❌ 失败: ${failed}/${emojiList.length}`);

  if (failedEmojis.length > 0) {
    console.log('\n❌ 失败的 Emoji:');
    failedEmojis.forEach(({ emoji, codepoint, error }) => {
      console.log(`  ${emoji} (${codepoint}): ${error}`);
    });
  }

  console.log(`\n💾 本地存储路径: ${CONFIG.LOCAL_DIR}`);
  return { success, failed, failedEmojis };
}

/**
 * 生成本地映射文件
 */
function generateLocalMap(emojiList) {
  console.log('\n🗺️ 生成本地 emoji 映射...');

  const localMap = {};

  emojiList.forEach(emoji => {
    const codepoint = getEmojiCodepoint(emoji);
    const localPath = path.join(CONFIG.LOCAL_DIR, `${codepoint}.png`);

    if (fs.existsSync(localPath)) {
      localMap[emoji] = {
        codepoint,
        localPath: `/assets/twemoji/${codepoint}.png`,
        exists: true
      };
    } else {
      localMap[emoji] = {
        codepoint,
        localPath: `/assets/twemoji/${codepoint}.png`,
        exists: false
      };
    }
  });

  const mapPath = path.join(CONFIG.LOCAL_DIR, 'emoji-local-map.json');
  fs.writeFileSync(mapPath, JSON.stringify(localMap, null, 2));

  const existingCount = Object.values(localMap).filter(item => item.exists).length;
  console.log(`✅ 本地映射已生成: ${existingCount}/${emojiList.length} 个 emoji`);

  return localMap;
}

